package day03_EscapeSequences;

public class EscapeSequences {
    /*
    \n Newline  Advences the cursor to the next line for subsequent printing
    \t Horizontal Tab
    \\ Backslash    Causes a backslash to be printed
    \" Double quote Causes a double quotation mark to be printed
     */

    public static void main(String[] args) {
        System.out.println("Java \nPhyton \nC++");
        System.out.println("----------------------");
        System.out.println("Hello Cydeo\n" +
                "How are you all today?\n" +
                "It's nice to see you all!\nWhat class do we have next week?");
        System.out.println("--------------------------------");
        System.out.println("\tJava is cool programming language.");
        System.out.println("\"\"\"");
        System.out.println("My favorite TV show is \"Matrix\"\n\tFirst one");






    }
}
